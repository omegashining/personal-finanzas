    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        double pe, por, p;
        p = Double.parseDouble(precio.getValueAt(0,0).toString());
        por = p * (otras.getCuenta("Costo de Ventas").saldo()/otras.getCuenta("Ventas").saldo());
        pe = otras.gastosDeOperacion()/(1-(por/p));

        campo.setValueAt(pe, 0, 1);
        campo.setValueAt(pe/p, 1, 1);
        campo.setValueAt((pe/p)*por, 2, 1);
    }//GEN-LAST:event_calcularActionPerformed


